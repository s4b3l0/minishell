#include <ft_printf.h>

void		ft_putpad(int n, t_struct *p)
{
	char c;

	c = ' ';
	if (p->lr == LEFT && p->zpad == YES)
		c = '0';
	while (n > 0)
	{
		ft_putchar(c);
		n--;
	}
}
