#include <libft.h>
#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	char	*c;
	size_t	len;

	len = -1;
	if (!n)
		return ;
	c = (char *)s;
	while (++len < n)
		*c++ = 0;
}
